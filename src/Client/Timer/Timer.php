namespace DragBox\Client\Timer;

use DragBox\Client\Client;
use DragBox\Common\Timer\TimerInterface;

/**
 * @codeCoverageIgnore
 */
abstract class Timer implements TimerInterface
{
    /**
     * @var Client
     */
    protected $client;

    public function __construct(Client $client)
    {
        $this->client = $client;
    }

    /**
     * @return Client
     */
    public function getClient()
    {
        return $this->client;
    }
}